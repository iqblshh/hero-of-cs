Maps = function(id,image,grid,x,y) { //load map image.

    var self = {
        id: id,
        image: image,
        grid: grid,
        x: x,
        y: y
    };

    self.width = 1280;
    self.height = 1280;

    self.updateMap = function() {

        self.updateMapPosition();
        self.drawMap();
    }

    self.isCollideWall = function(pt) {

        var gridX = Math.floor(pt.x / TILE_SIZE);
        var gridY = Math.floor(pt.y / TILE_SIZE);

        if(gridX < 0 || gridX >= self.grid[0].length) {
            return true;
        }
        if(gridY < 0 || gridY >= self.grid.length) {
            return true;
        }

        return self.grid[gridY][gridX]
    }

    self.updateMapPosition = function() {

        if(player.x >= WIDTH/2) {
            self.x =  WIDTH/2 - player.x;
        }
        else {
            self.x = 0;
        }

        if(player.y >= HEIGHT/2) {
            self.y =  HEIGHT/2 - player.y;
        }
        else {
            self.y = 0;
        }

        if(self.x <= WIDTH - self.width) {
            self.x = WIDTH - self.width;
        }

        if(self.y <= HEIGHT - self.height) {
            self.y = HEIGHT - self.height;
        }
    }

    self.drawMap = function() {

        ctx.drawImage(self.image,0,0,self.width,self.height,self.x,self.y,self.width,self.height);
    }

    return self;
}

